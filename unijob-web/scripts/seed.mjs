async function seedUsers() {
  console.log('\n--- Seeding users...');
  for (const user of USERS) {
    const now = Timestamp.now();
    await db.collection('users').doc(user.uid).set({ ...user, createdAt: now, updatedAt: now });
    console.log(`  + ${user.displayName} (${user.uid})`);
  }
}

async function seedJobs() {
  console.log('\n--- Seeding jobs...');
  const jobIds = {};
  for (const job of JOBS) {
    const ref = await db.collection('jobs').add(job);
    jobIds[job.title] = ref.id;
    const tag = job.isUrgent ? 'URGENT' : job.status.toUpperCase();
    console.log(`  + [${tag}] ${job.title}`);
  }
  return jobIds;
}

async function seedApplications(jobIds) {
  console.log('\n--- Seeding applications...');
  let count = 0;
  for (const app of APPLICATIONS_EXTRA) {
    const jobId = jobIds[app.jobTitle];
    if (!jobId) { console.warn(`  ! Job not found: ${app.jobTitle}`); continue; }
    const now = Timestamp.now();
    await db.collection('applications').add({
      jobId,
      applicantId: app.applicantId,
      applicantName: app.applicantName,
      applicantPhoto: app.applicantPhoto,
      message: app.message,
      status: app.status,
      createdAt: now,
      updatedAt: now,
    });
    count++;
    console.log(`  + ${app.applicantName} -> ${app.jobTitle.substring(0, 45)}...`);
  }
  return count;
}

async function seedRatingsAndCompletions(jobIds) {
  console.log('\n--- Seeding ratings & completions...');
  const completedTitles = JOBS.filter((j) => j.status === 'completed').map((j) => j.title);

  for (const title of completedTitles) {
    const jobId = jobIds[title];
    if (!jobId) continue;
    await db.collection('jobCompletions').add({
      jobId,
      workerConfirmed: true,
      posterConfirmed: true,
      completedAt: Timestamp.now(),
      status: 'confirmed',
    });
    console.log(`  + Completion: ${title.substring(0, 45)}...`);
  }

  let ratingCount = 0;
  for (const r of RATINGS_DATA) {
    const jobId = jobIds[r.jobTitle];
    if (!jobId) continue;
    await db.collection('ratings').add({
      jobId,
      fromUserId: r.fromUserId,
      toUserId: r.toUserId,
      score: r.score,
      comment: r.comment,
      type: r.type,
      createdAt: Timestamp.now(),
    });
    ratingCount++;
  }
  console.log(`  + ${ratingCount} ratings created`);
  return ratingCount;
}

async function seedWorkHistory(jobIds) {
  console.log('\n--- Seeding work history...');
  let count = 0;
  for (const wh of WORK_HISTORY) {
    const jobId = jobIds[wh.jobTitle];
    if (!jobId) continue;
    await db.collection('workHistory').add({
      user1: wh.user1,
      user2: wh.user2,
      jobId,
      completedAt: Timestamp.now(),
      rating: wh.rating,
    });
    count++;
  }
  console.log(`  + ${count} work history records created`);
  return count;
}

